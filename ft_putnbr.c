#include "libft.h"

void	ft_putnbr(int n)
{
	long int ln;

	ln = n;
	if (ln < 0)
	{
		ln = -ln;
		ft_putchar('-');
	}
	if (ln > 9)
	{
		ft_putnbr(ln / 10);
		ft_putnbr(ln % 10);
	}
	else
	{
		ft_putchar(ln + '0');
	}
}
