#include "libft.h"

char	*ft_strcat(char *to, const char *from)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (to[i])
		i++;
	while (from[j])
	{
		to[i] = from[j];
		i++;
		j++;
	}
	to[i] = '\0';
	return (to);
}
