#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int ln;

	ln = n;
	if (ln < 0)
	{
		ln = -ln;
		ft_putchar_fd('-', fd);
	}
	if (ln > 9)
	{
		ft_putnbr_fd(ln / 10, fd);
		ft_putnbr_fd(ln % 10, fd);
	}
	else
	{
		ft_putchar_fd(ln + '0', fd);
	}
}
