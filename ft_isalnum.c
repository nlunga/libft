#include "libft.h"

int	ft_isalnum(int nb)
{
	if (nb >= '0' && nb <= '9')
		return (1);
	else if (nb >= 'A' && nb <= 'Z')
		return (1);
	else if (nb >= 'a' && nb <= 'z')
		return (1);
	else
		return (0);
}
